//! Central registry of agent tools with schema and execution metadata.

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ToolKind {
    Read,
    Write,
}

/// Execution mode for tool dispatch.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ExecutionMode {
    /// Execute immediately; no user approval.
    ReadAuto,
    /// Must wait for user approval before execution.
    ApprovalRequired,
}

#[derive(Debug, Clone)]
#[allow(dead_code)]
pub struct ToolDef {
    pub name: &'static str,
    pub kind: ToolKind,
    pub description: &'static str,
    pub parameters: &'static str, // JSON Schema string
    pub execution_mode: ExecutionMode,
    pub requires_wallet: bool,
    pub supports_multi_wallet: bool,
    pub supports_synthesis: bool,
    pub example: &'static str,
    /// Bundled app id when this tool is gated (`None` = always available).
    pub required_app_id: Option<&'static str>,
    /// Subset of manifest permission ids that must be granted in SQLite.
    pub required_permission_ids: &'static [&'static str],
}

